import math

rad_angle=math.radians(0)   #0파이
print(rad_angle)

rad_angle=math.radians(90)  #1파이/2==90도
print(rad_angle)            #3.1415.../2

rad_angle=math.radians(180) #1파이==180도
print(rad_angle)            #3.141519...

rad_angle=math.radians(270) #3파이/2==270도
print(rad_angle)            #3.1415...*3/2